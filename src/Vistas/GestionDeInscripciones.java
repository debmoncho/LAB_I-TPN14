    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = jTabla.getSelectedRow();
        
        if(filaSeleccionada != -1){
            Alumno a = (Alumno)cboxAlumno.getSelectedItem();
            int idMateria = (Integer)modelo.getValueAt(filaSeleccionada, 0);
            String nombreDeMateria = (String)modelo.getValueAt(filaSeleccionada, 1);
            int anio = (Integer)modelo.getValueAt(filaSeleccionada, 2);
            Materia m = new Materia(idMateria, nombreDeMateria, anio, true);
            Inscripcion i = new Inscripcion(0, a, m);
            inscData.guardarInscripcion(i);
            borrarFilasTabla();
        }
    }//GEN-LAST:event_jbInscribirActionPerformed

    private void jbAnularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAnularActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = jTabla.getSelectedRow();
        
        if(filaSeleccionada != -1){
            Alumno a = (Alumno)cboxAlumno.getSelectedItem();
            int idMateria = (Integer)modelo.getValueAt(filaSeleccionada, 0);
            inscData.borrarInscripcion(a.getIdAlumno(), idMateria);
            borrarFilasTabla();
            
        }
    }//GEN-LAST:event_jbAnularActionPerformed

